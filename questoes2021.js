const questionTitle = document.getElementById("question-title");
const questionElement = document.getElementById("question");
const answereButtons = document.getElementById("answer-buttons");
const nextButton = document.getElementById("next-btn");
const backButton = document.getElementById("back-btn");
const placar = document.getElementById("placar");

//paginação
const qst1 = document.getElementById("qst-1");
const qst2 = document.getElementById("qst-2");
const qst3 = document.getElementById("qst-3");

//index que corre pelo arrey
let currentQuestionIndex = 0;

//pontuação
let score = 0;

//Função para iniciar o teste
function startQuiz(){
currentQuestionIndex = 0;
score = 0;
nextButton.innerHTML = "Proxima";
backButton.innerHTML = "Anterior"
showQuestion();
}

function showQuestion(){
resetState();
let currentQuestion = questoes2021[currentQuestionIndex];
let questionNo = questoes2021[currentQuestionIndex].id;
questionElement.innerHTML = "(ENARE - 2021) " + currentQuestion.questao;
questionTitle.innerHTML = "Questão " + questionNo;
placar.innerHTML = "Pontos: " + score;


currentQuestion.answers.forEach(answer => {
  const button = document.createElement("button");
  button.innerHTML = answer.text;
  button.classList.add("btn");
  answereButtons.appendChild(button);
  if(answer.correct){
    button.dataset.correct = answer.correct;
  }
  button.addEventListener("click", selectAnswer);
});
}
function resetState(){
nextButton.style.display = "none";
backButton.style.display = "none";
while(answereButtons.firstElementChild){
  answereButtons.removeChild(answereButtons.firstChild);
}
}

function selectAnswer(e){
const selectedBtn = e.target;
const isCorrect = selectedBtn.dataset.correct === "true";
if (isCorrect) {
  selectedBtn.classList.add("correct");
  score = score + questoes2021[currentQuestionIndex].pontos;
  questoes2021[currentQuestionIndex].pontos = 0;
}else{
  selectedBtn.classList.add("incorrect");
}
Array.from(answereButtons.children).forEach(button => {
    if(button.dataset.correct === "true"){
      button.classList.add("correct");
    }
    button.disabled = true;
});
nextButton.style.display = "block";
}

function showScore(){
resetState();
questionElement.innerHTML = `Sua pontuação foi ${placar} de 80 !`;
questionTitle.innerHTML = "Fim do simulado!"
nextButton.innerHTML = "Começar novamente!";
nextButton.style.display = "block";
}

//Botão Anterior
function handleBackButton(){
  scroll(0,0);
  currentQuestionIndex--;

if (currentQuestionIndex < questoes2021.length){
  showQuestion();    
  if(currentQuestionIndex <= 0){
    backButton.style.display = "none";
  }else{    
  backButton.style.display = "block";
  }
}else{
  showScore();
}
}
backButton.addEventListener("click", ()=>{
  if (currentQuestionIndex < questoes2021.length) {
    handleBackButton();      
  }else{
    startQuiz()
  }
}); 
startQuiz();

//Botão Proximo
function handleNextButton(){
scroll(0,0);
currentQuestionIndex++;
if (currentQuestionIndex < questoes2021.length){
  showQuestion();
  if(currentQuestionIndex <= 0){
    backButton.style.display = "none";
  }else{    
  backButton.style.display = "block";
  }
}else{
  showScore();
}
}

nextButton.addEventListener("click", ()=>{
  if (currentQuestionIndex < questoes2021.length) {
    handleNextButton();      
  }else{
    location.reload();
  }

});

startQuiz();

